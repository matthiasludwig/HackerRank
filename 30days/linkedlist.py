class Node:
    def __init__(self,data):
        self.data = data
        self.next = None 
class Solution: 
    def display(self,head):
        current = head
        while current:
            print(current.data,end=' ')
            current = current.next

    def insert(self,head,data): 
        #Complete this method
        T = Node(data)

        if (head != None):
            head.next = T
            head.next = None

        print("T created: {} | {}".format(T.data, T.next))

        return T



